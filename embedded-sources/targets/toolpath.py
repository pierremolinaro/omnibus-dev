import os

#----------------------------------------------------------------------------------------------------------------------*
#   Tool dir                                                                                                           *
#----------------------------------------------------------------------------------------------------------------------*

def toolDir ():
  (SYSTEM_NAME, MODE_NAME, RELEASE, VERSION, MACHINE) = os.uname ()
  if SYSTEM_NAME == "Darwin":
    MACHINE = "i386"
  return os.path.expanduser ("~/plm-tools/plm-" + SYSTEM_NAME + "-" + MACHINE + "-llvm-3.8.0-binutils-2.26-openocd-0.8.0-libusb-1.0.19")

